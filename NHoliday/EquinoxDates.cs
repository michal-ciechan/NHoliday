        public static DateTime GetVernalEquinoxDateTime(int year)
        {
            if (year < 1788)
                throw new Exception("Cannot get equinox date time for a year before 1788");

            if (year > 2211)
                throw new Exception("Cannot get equinox date time for a year after 2211");

            return EquinoxDateTimes[year - 1788];
        }

